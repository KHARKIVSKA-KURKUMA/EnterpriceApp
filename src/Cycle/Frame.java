    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed

        double x = Double.valueOf(txtX.getText().trim());
        int n = Integer.valueOf(txtN.getText().trim());
        progressBar.setMaximum(n);  
        double
            q,		
            a1,		
            a2;		
        double S = 1;   
        a1 = 1;         
        for (int i = 0; i < n; i++) 
        {
           q = x / (i+1);    
           a2 = a1 * q;        
           S += a2;            
           a1 = a2;            
           progressBar.setValue(i+1);   
        }

        txtS.setText(String.format("%15.6e", S)); 
        txtA.setText(String.format("%15.6e", a1));    
    }//GEN-LAST:event_btnCalcActionPerformed

